// 1. Check if Two Numbers Are Equal
let num1 = 10;
let num2 = 10;
console.log("Are numbers equal? ", num1 == num2); // true

// 2. Check if a Number is Greater Than Another Number
let a = 15;
let b = 7;
console.log("Is a greater than b? ", a > b); // true

// 3. Check if a Number is Less Than or Equal to Another Number
let x = 20;
let y = 25;
console.log("Is x less than or equal to y? ", x <= y); // true

// 4. Check if Two Numbers Are Not Equal
let m = 5;
let n = 9;
console.log("Are m and n not equal? ", m != n); // true

// 5. Check if a Number is Greater Than or Equal to 100
let score = 120;
console.log("Is score >= 100? ", score >= 100); // true
