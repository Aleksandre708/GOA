# 1. Step: Create an array of fruits
fruits = ["apple", "banana", "cherry", "mango", "orange"]

# 2. Step: Print the first fruit
print("First fruit:", fruits[0])  # Index 0

# 3. Step: Print the last fruit
print("Last fruit:", fruits[-1])  # Negative index accesses from end

# 4. Step: Print the total number of fruits
print("Total number of fruits:", len(fruits))  # len() gives length
